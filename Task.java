public class Task {

    private String name;
    private int theEmployees;
    private int theResources;


    public String getName() {
        return name;
    }
    public void setName(String x) {
        this.name = x;
    }

    public int getTheEmployees() {
        return theEmployees;
    }
    public void setTheEmployees(int y) {
        this.theEmployees = y;
    }

    public int getTheResources() {
        return theResources;
    }
    public void setTheResources(int z) {
        this.theResources = z;
    }

}