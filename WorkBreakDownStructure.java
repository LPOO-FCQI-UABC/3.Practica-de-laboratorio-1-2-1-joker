public class WorkBreakDownStructure {

	private int name;
	private int theTasks;

	public int getName() {
		return name;
	}

	/**
	 * 
	 * @param name
	 */
	public void setName(int name) {
		this.name = name;
	}

	public int getTheTasks() {
		return theTasks;
	}

	/**
	 * 
	 * @param theTasks
	 */
	public void setTheTasks(int theTasks) {
		this.theTasks = theTasks;
	}

}